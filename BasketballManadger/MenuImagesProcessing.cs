using CsvHelper;
using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.Globalization;
using System.IO;
using System.Text;

namespace BasketballManadger
{
    public class MenuImagesProcessing
    {
        private string _fileWithData = @"C:\Users\Zhenya\source\repos\BasketballManadger\BasketballManadger\Menu Images\MenuImages.csv";

        public string ImageCSV { get; set; }
        public string ImageTXT { get; set; }
        public string ImageJSON { get; set; }
        public string ImageXLSX { get; set; }
        public string ImageXML { get; set; }


        public BindingList<MenuImagesProcessing> GetImagesFromFile()
        {
            BindingList<MenuImagesProcessing> imagesToOutput = new BindingList<MenuImagesProcessing>();
            using var streamReader = File.OpenText(_fileWithData);
            using var csvReader = new CsvReader(streamReader, CultureInfo.CurrentCulture);

            var images = csvReader.GetRecords<MenuImagesProcessing>();

            foreach (var item in images)
            {
                imagesToOutput.Add(item);
            }

            return imagesToOutput;
        }
        public MenuImagesProcessing()
        {

        }

    }
}
